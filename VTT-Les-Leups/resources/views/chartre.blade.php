<br><br>

<p class="text-xl font-bold">
    Quels sont les cookies sucepibles d'être installés lorsque vous visitez notre site ou notre application ?
</p>
<p class="text-xl">
Dans notre cas, nous utilisons des cookies et des trackeurs afin de vous aider à naviguer sur notre site et application, et vous proposer des services mieux adaptés à vos demandes en connaissant vos habitudes de navigation. Les cookies nous permettent également de disposer d’outils statistiques sur l’usage de notre site et application afin d’améliorer ceux-ci régulièrement.
</p>

<br><br>

<p class="text-xl font-bold">
    Durée de vie des cookies ?
</p>
<p class="text-xl">
Il existe deux types de cookies : les cookies de session et les cookies persistants. Les cookies de session sont créés de manière temporaire dans le sous-dossier de votre navigateur au moment où vous visitez un site web. Une fois que vous quittez le site, le cookie de session est supprimé.
</p>

<br><br>

<p class="text-xl">
Les fichiers du cookie persistant restent dans le sous-dossier de votre navigateur et ils sont réactivés une fois que vous consultez le site web qui a créé ce cookie spécifique. Un cookie persistant demeure dans le sous-dossier du navigateur pour la durée définie dans le fichier du cookie. D’une manière générale, la durée de validité d’un cookie est de 13 mois maximum.
</p>

<br><br>

<p class="text-xl font-bold">
    Puis-je refuser le dépôt de cookies sur mon ordinateur ?
</p>
<p class="text-xl">
Vous n’êtes pas obligés d’accepter le dépôt des cookies sur votre terminal. Néanmoins, nous ne pouvons vous garantir une expérience utilisateur identique si vous procédez à la suppression des cookies.
</p>

