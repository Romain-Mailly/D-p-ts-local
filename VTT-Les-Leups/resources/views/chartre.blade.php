<span class="text-2xl bg-yellow-500 xl:px-3 xl:py-3 rounded">Cookies</span>
</div>

<br><br>

<p class="text-xl">
Des cookies et des traceurs peuvent s’installer automatiquement sur votre logiciel de navigation ou votre terminal, lors de votre accès à notre site ou à notre application. Un cookie est un élément qui ne permet pas d’identifier une personne mais sert à enregistrer des informations relatives à la navigation de celle-ci sur le site  <a href="/" class="text-yellow-500 hover:underline focus:text-yellow-700" target="_blank" rel="noopener noreferrer">www.vttlesleups.fr</a>.
